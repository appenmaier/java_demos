package util;

/**
 * Simple Calculator
 *
 * @author Daniel Appenmaier
 * @version 1.0
 *
 */
public class Calculator {

   /**
    * Adds two integers
    *
    * @param a integer 1
    * @param b integer 2
    * @return sum of a and b
    */
   public static int add(int a, int b) {
      return a + b;
   }

   /**
    * Multiplies two integers
    *
    * @param a integer 1
    * @param b integer 2
    * @return product of a and b
    */
   public static int multiply(int a, int b) {
      return a * b;
   }

}
